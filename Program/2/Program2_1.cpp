#include <iostream>                // 宣告資料以及控制輸入輸出
using namespace std;               // 

int main(void)                     // 結果的資料為 int 函數名稱叫做 main 沒有任何預先輸入的變數
{
    cout << "Hello there world!";  // 使用 ""輸入字串 
    return 0;                      // 每個指令以分號結束
}

/*
    變數由文字、數字、底線的任意組合所構成
    1. 第一個字元必須為文字或底線
    2. 沒有空白或特殊字元
    3. 不能是關鍵字
    4. 最大長度為 1024
    5. 區分大小寫
*/